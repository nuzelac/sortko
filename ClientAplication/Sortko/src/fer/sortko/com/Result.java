package fer.sortko.com;

public class Result{
	private String resultPlace;
	private String resultUser;
	private String resultNumber;
	    
	public String getResultUser() {
		return resultUser;
	}
	public void setResultUser(String resultUser) {
		this.resultUser = resultUser;
	}
	public String getResultNumber() {
		return resultNumber;
	}
	public void setResultNumber(String resultNumber) {
		this.resultNumber = resultNumber;
	}
	public void setResultPlace(String resultPlace) {
		this.resultPlace = resultPlace;
	}
	public String getResultPlace() {
		return resultPlace;
	}
}
